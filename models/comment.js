"use strict"

// ****************************************************************************
// ****************************************************************************
// Imports

const mongoose = require( "mongoose" )




// ****************************************************************************
// ****************************************************************************
// Schema definition

const commentSchema = mongoose.Schema({
  text  : String,
  author: {
    id: {
      type: mongoose.Schema.Types.ObjectId,
      ref : "User"
    },
    username: "String"
  }
})




// ****************************************************************************
// ****************************************************************************
// Exports

module.exports = mongoose.model( "Comment", commentSchema )
